// Language Switcher
function initLanguageSwitcher() {
    langButtons.forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            langButtons.forEach(b => b.classList.remove('active'));
            this.classList.add('active');
            setLanguage(lang);
        });
    });
}

// Set Language
function setLanguage(lang) {
    currentLang = lang;
    const translations = i18n[lang];
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[key]) {
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = translations[key];
            } else if (element.tagName === 'OPTION') {
                element.textContent = translations[key];
            } else {
                element.textContent = translations[key];
            }
        }
    });
    
    // Update location options
    const locationSelect = document.getElementById('location');
    if (locationSelect && translations.cityNames) {
        const selectedValue = locationSelect.value;
        Array.from(locationSelect.options).forEach(option => {
            const value = option.value;
            if (translations.cityNames[value]) {
                option.textContent = translations.cityNames[value];
            }
        });
        // Restore selected value
        locationSelect.value = selectedValue;
    }
}

// Initialize Modal
function initModal() {
    // Contact button click
    contactBtn.addEventListener('click', () => {
        openModal();
        resetPopupTimer();
    });
    
    // Footer contact button click
    footerContactBtn.addEventListener('click', () => {
        openModal();
        resetPopupTimer();
    });
    
    // Close modal button
    modalClose.addEventListener('click', closeModal);
    
    // Close modal on overlay click
    formModal.addEventListener('click', (e) => {
        if (e.target === formModal) {
            closeModal();
        }
    });
    
    // Close modal on ESC key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && formModal.classList.contains('active')) {
            closeModal();
        }
    });
}

function openModal() {
    formModal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    formModal.classList.remove('active');
    document.body.style.overflow = 'auto';
}

// Popup Timer Functions
function startPopupTimer() {
    popupTimer = setTimeout(() => {
        if (!formSubmitted && !formModal.classList.contains('active')) {
            openModal();
        }
    }, 10000); // 10 seconds
}

function resetPopupTimer() {
    if (popupTimer) {
        clearTimeout(popupTimer);
        popupTimer = null;
    }
}

// Form Validation
